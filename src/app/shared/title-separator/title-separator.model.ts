/**
 * Données contenues dans un titre séparateur de sections
 */
export interface TitleSeparator {
  /**
   * Titre
   */
  heading: string;

  /**
   * Sous-titre, généralement un mot, à afficher avant le titre
   */
  subtitle: string;

  /**
   * Petite description à afficher sur la section
   */
  description: string;
}
